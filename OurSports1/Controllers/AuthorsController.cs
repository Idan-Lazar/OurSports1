using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using OurSports1.Data;
using OurSports1.Models;
using Microsoft.AspNetCore.Authorization;

namespace OurSports1.Controllers
{
    [Authorize(Roles = "Admin")]
    public class AuthorsController : Controller
    {
        private readonly ApplicationDbContext _context;

        public AuthorsController(ApplicationDbContext context)
        {
            _context = context;
        }

        // GET: Authors
        public async Task<IActionResult> Index()
        {
            return View(await _context.Author.ToListAsync());
        }

        public async Task<List<Comment>> AuthorsAjax(int Authorid)
        {

            var a = (from author in _context.Author
                     where author.ID == Authorid
                     join article in _context.Article on author.ID equals article.AuthorID
                     join comment in _context.Comment on article.ID equals comment.ArticleID
                     select comment).Distinct<Comment>().Take(5); 
           
                       return (await a.ToListAsync());
        }

        // GET: Authors/Details/5
        [AllowAnonymous]
        public async Task<IActionResult> Details(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var author = await _context.Author
                .SingleOrDefaultAsync(m => m.ID == id);
            var a = (from Article in _context.Article select Article).Where(m => m.AuthorID == id);
            ICollection<Article> articles = a.ToArray<Article>();
            /* foreach ( Article m in articles )
